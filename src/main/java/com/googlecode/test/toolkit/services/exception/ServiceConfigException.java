package com.googlecode.test.toolkit.services.exception;

public class ServiceConfigException extends ServiceException {

	private static final long serialVersionUID = -1;

	public ServiceConfigException() {
 	}

	public ServiceConfigException(String arg0) {
		super(arg0);
 	}

	public ServiceConfigException(Throwable arg0) {
		super(arg0);
 	}

	public ServiceConfigException(String arg0, Throwable arg1) {
		super(arg0, arg1);
 	}

}
